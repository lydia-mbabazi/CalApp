public class Calculator {
    public int add(int firstnum,int secondnum){
       int resultsum= firstnum + secondnum;
       return resultsum;
    }
    public int sub(int firstnum,int secondnum){
        int resultsub= firstnum - secondnum;
        return resultsub;
    }
    public float div(int firstnum,int secondnum){
        float resultdiv= firstnum / secondnum;
        return resultdiv;
    }
    public int mult(int firstnum,int secondnum){
        int resultmult= firstnum * secondnum;
        return resultmult;
    }
}
